## calculate the inverse matrix and store it to the cache
## which can be retrived later.

## a function include a list of functions, which set and get a matrix 
## the matrix is the inverse of the input matrix for the purpose of the 
## assignment
makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) m <<- solve
  getsolve <- function() m
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}


## check if the inverse is stored in the cache, 
## otherwise, calculate the inverse matrix and stored it
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getsolve()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setsolve(m)
  m
}
